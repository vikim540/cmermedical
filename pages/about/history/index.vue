        ctx.quadraticCurveTo(width / 4 + offset, height / 2 - Math.sin(num)*240, width / 2 + offset, height / 2);
        ctx.quadraticCurveTo((3 / 4) * width + offset, height / 2 + Math.sin(num)*240, width + offset, height / 2);
        ctx.stroke();

       

        offset += 4;
